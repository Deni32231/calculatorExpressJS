class Calculator {
  static toAdd(a: number, b: number) {
    return a + b;
  }

  static toSubtract(a: number, b: number) {
    return a - b;
  }

  static toMultiply(a: number, b: number) {
    return a * b;
  }

  static toDivide(a: number, b: number) {
    return a / b;
  }
}

export default Calculator;
